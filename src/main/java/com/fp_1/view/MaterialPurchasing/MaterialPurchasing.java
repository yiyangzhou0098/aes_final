    public void updateDB() throws ClassNotFoundException, SQLException{

        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlConn=DriverManager.getConnection(dataConn,username,password);
            pst=sqlConn.prepareStatement("select * from connector");
            
            rs=pst.executeQuery();
            ResultSetMetaData stData=rs.getMetaData();
            
            int q=stData.getColumnCount();
            DefaultTableModel RecordTable= (DefaultTableModel)jTable1.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next())
            {
                Vector columnData=new Vector();
                for (i=1;i<=q;i++)
                {
                    columnData.add(rs.getString("OrderNumber"));
                    columnData.add(rs.getString("MaterialName"));
                    columnData.add(rs.getString("Origin"));
                    columnData.add(rs.getString("Weight"));
                    columnData.add(rs.getString("UnitPrice"));
                    columnData.add(rs.getString("ShipmentFee"));
                    columnData.add(rs.getString("OrderDate"));
                    columnData.add(rs.getString("TimeSpand"));
                    columnData.add(rs.getString("IsArrived"));
                    //columnData.add(rs.getString("IsArrived"));
                }
                RecordTable.addRow(columnData);
                
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
            
        }
